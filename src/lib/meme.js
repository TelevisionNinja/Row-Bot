// posts a daily meme
export async function getMeme() {
    const URL = `${memes.URLs[randomInteger(memes.URLs.length)]}${memes.queryString}${memes.postCount}`;
    let memeURL = '';

    await queue.add(async () => {
        const response = await nodeFetch(URL, {
            headers: {
                'User-Agent': userAgents[randomInteger(userAgents.length)] // reddit api requires a user agent. some are blocked thus the user of browser user agents
            }
        });

        if (backOff(response, queue)) {
            return;
        }

        try {
            const postArr = (await response.json()).data.children;
            const post = postArr[randomInteger(memes.postCount)];

            memeURL = post.data.url;
        }
        catch (error) {
            console.log(error);
        }
    });

    return memeURL;
}
